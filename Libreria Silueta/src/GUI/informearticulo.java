    public void llenartabla()
    {
        //limpiar tabla
        int filas=tabla.getRowCount();
            for (int i = 0;filas>i; i++) {
                tabla.removeRow(0);
            }
            //llenado de tabla
        for(producto p:this.prods.getlistaproductos(this.c.getConexion(),this.c.getEstado()))
           {
               tabla.addRow(new Object[]{p.getnombre(),p.getproveedor().getnombre(),p.getcategoria().getcategoria(),p.getdescripcion(),p.getprec_uni(),p.getunidadmedida(),p.getcantidad()});
           }  
    }
