    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
       if(txtnombre.getText().equals("") || txtapellido.getText().equals("") ||
          txtdireccion.getText().equals("") || txtciudad.getText().equals("") ||
          txtpais.getText().equals("") || txttelefoo.getText().equals("") ||
          labelcat.getText().equals("") || txtdescuento.getText().equals("")||
          txtvisitas.getText().equals(""))
       {
           JOptionPane.showMessageDialog(this,"sericiorese de haber llenado el formulario");
       }
       else
       {
           llenarcliente();
           cli.insertar(this.c.getConexion(), this.c.getEstado());
           jButton4.setEnabled(true);
           combocategoria.setEnabled(false);
           btnagregar.setEnabled(false);
           cancelar.setEnabled(false);
           this.clients = this.clits.getclientes(this.c.getConexion(), this.c.getEstado());
           llenartabla();
       }
    }//GEN-LAST:event_btnagregarActionPerformed

