    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //this.clients = clits.getclientes(this.c.getConexion(), this.c.getEstado());
        if(cli.getid() == clients.get(0).getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al principio");
        }
        else
        {
            int index = 0;
            for(cliente c:clients)
            {
                if(c.getid() == cli.getid())
                {
                    break;
                }
                index += 1;
            }
            cli = clients.get(index - 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

