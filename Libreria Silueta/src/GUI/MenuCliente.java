    private void txtbnombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbnombreKeyReleased
        String letra = "";
        letra = txtbnombre.getText();
        if(letra.equals(""))
        {
           // this.clients.clear();
            this.clients = this.clits.getclientes(this.c.getConexion(), this.c.getEstado());
            this.llenartabla();
        }
        else
        {    
           //this.clients.clear();
           this.clients = this.clits.getclientesporletra(letra, this.c.getConexion(), this.c.getEstado());
           this.llenartabla();
           this.cli = this.clients.get(0);
           this.ingresardatos();
        } 
    }//GEN-LAST:event_txtbnombreKeyReleased

