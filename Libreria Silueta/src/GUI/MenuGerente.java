    public void ingresardatos()
    {
           txtnombre.setText(g.getnombre());
           txtapellido.setText(g.getapellido());
           txtdireccion.setText(g.getdireccion());
           txtciudad.setText(g.getciudad());
           txtpais.setText(g.getpais());
           txttelefoo.setText(g.gettelefono() + "");
           txtclave.setText(g.getclave());
    }
    
    public void llenargerente()
    {
           g.setnombre(txtnombre.getText());
           g.setapellido(txtapellido.getText());
           g.setdireccion(txtdireccion.getText());
           g.setciudad(txtciudad.getText());
           g.setpais(txtpais.getText());
           g.setteleffono(Integer.parseInt(txttelefoo.getText()));
           g.setclave(txtclave.getText());           
    }
