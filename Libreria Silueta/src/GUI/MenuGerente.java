    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        this.gerents = gs.getlistagerentes(this.c.getConexion(), this.c.getEstado());
        if(gerents.get(this.gerents.size()-1).getid() == g.getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al final");
        }
        else
        {
            int index = 0;
            for(gerente gg:gerents)
            {
                if(gg.getid() == g.getid())
                {
                    break;
                }
                index += 1;
            }
            g = gerents.get(index + 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        JOptionPane.showMessageDialog(this,g.borrar(this.c.getConexion(), this.c.getEstado()));
        txtnombre.setText("");
        txtapellido.setText("");
        txtdireccion.setText("");
        txtciudad.setText("");
        txtpais.setText("");
        txttelefoo.setText("");
        txtclave.setText("");
        //llenare de el primer cliente que encuentre para evitar  errores de navegacion cuando el cli este vacio
        for(gerente gg:this.gs.getlistagerentes(this.c.getConexion(), this.c.getEstado()))
        {
            this.g = gg;
            break;
        }
        ingresardatos();
    }//GEN-LAST:event_jButton6ActionPerformed
    boolean bandera = false;
    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        if(bandera == false)
        {
            btnmodificar.setText("Finalizar Modificado");
            bandera = true;
        }
        else
        {
            if(txtnombre.getText().equals("") || txtapellido.getText().equals("") ||
            txtdireccion.getText().equals("") || txtciudad.getText().equals("") ||
            txtpais.getText().equals("") || txttelefoo.getText().equals("") ||
            txtclave.getText().equals(""))
            {
               JOptionPane.showMessageDialog(this,"sericiorese de haber llenado el formulario");
            }
            else
            {
                llenargerente();
                g.actualizar(this.c.getConexion(), this.c.getEstado());
                btnmodificar.setText("Modificar");
            }
            bandera = false;
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         this.gerents = gs.getlistagerentes(this.c.getConexion(), this.c.getEstado());
        if(g.getid() == gerents.get(0).getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al principio");
        }
        else
        {
            int index = 0;
            for(gerente gg:gerents)
            {
                if(gg.getid() == g.getid())
                {
                    break;
                }
                index += 1;
            }
            g = gerents.get(index - 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        if(txtnombre.getText().equals("") || txtapellido.getText().equals("") ||
            txtdireccion.getText().equals("") || txtciudad.getText().equals("") ||
            txtpais.getText().equals("") || txttelefoo.getText().equals("") ||
            txtclave.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"sericiorese de haber llenado el formulario");
        }
        else
        {
            llenargerente();
            g.insertar(this.c.getConexion(), this.c.getEstado());
            jButton4.setEnabled(true);
            btnagregar.setEnabled(false);
            cancelar.setEnabled(false);
        }
    }//GEN-LAST:event_btnagregarActionPerformed

