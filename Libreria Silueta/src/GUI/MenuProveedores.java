    public MenuProveedores(conexion c) {
        initComponents();
        setLocationRelativeTo(null);
        setResizable(false);
        this.c = c;
        this.pro = new proveedor();
        this.pros = new proveedores();
        this.provs = new ArrayList<proveedor>();
        this.provs = pros.getproveedores(this.c.getConexion(), this.c.getEstado());
        if(this.provs.size() == 0)
        {
            JOptionPane.showMessageDialog(this,"Presione ingresar nuevo para ingresar un proveedor");
        }
        else
        {
             for(proveedor p:this.pros.getproveedores(this.c.getConexion(), this.c.getEstado()))
             {
                 this.pro = p;
                 break;
             }
             ingresardatos();    
        }
        btnagregar.setEnabled(false);
        cancelar.setEnabled(false);
    }

    public void ingresardatos()
    {
           txtombre.setText(pro.getnombre());
           txtapellido.setText(pro.getapellido());
           txtdireccion.setText(pro.getdireccion());
           txtciudad.setText(pro.getciudad());
           txtpais.setText(pro.getpais());
           txttelefono.setText(pro.gettelefono() + "");
    }
    public void llenarproveedor()
    {
           pro.setnombre(txtombre.getText());
           pro.setapellido(txtapellido.getText());
           pro.setdireccion(txtdireccion.getText());
           pro.setciudad(txtciudad.getText());
           pro.setpais(txtpais.getText());
           pro.setteleffono(Integer.parseInt(txttelefono.getText()));
    }
