    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.provs = pros.getproveedores(this.c.getConexion(), this.c.getEstado());
        if(pro.getid() == provs.get(0).getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al principio");
        }
        else
        {
            int index = 0;
            for(proveedor p:provs)
            {
                if(p.getid() == pro.getid())
                {
                    break;
                }
                index += 1;
            }
            pro = provs.get(index - 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        this.provs = pros.getproveedores(this.c.getConexion(), this.c.getEstado());
        if(provs.get(this.provs.size()-1).getid() == pro.getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al final");
        }
        else
        {
             int index = 0;
            for(proveedor p:provs)
            {
                if(p.getid() == pro.getid())
                {
                    break;
                }
                index += 1;
            }
            pro = provs.get(index + 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed
    boolean bandera = false;
    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        if(bandera == false)
        {
          btnmodificar.setText("Finalizar Modificado");
          bandera = true;
        }
        else
        {
           if(txtombre.getText().equals("") || txtapellido.getText().equals("") ||
          txtdireccion.getText().equals("") || txtciudad.getText().equals("") ||
          txtpais.getText().equals("") || txttelefono.getText().equals(""))
          {
           JOptionPane.showMessageDialog(this,"sericiorese de haber llenado el formulario");
          } 
           else
           {
               llenarproveedor();
               pro.actualizar(this.c.getConexion(), this.c.getEstado());
               btnmodificar.setText("Modificar");
           }
           bandera = false;
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        if(txtombre.getText().equals("") || txtapellido.getText().equals("") ||
          txtdireccion.getText().equals("") || txtciudad.getText().equals("") ||
          txtpais.getText().equals("") || txttelefono.getText().equals(""))
       {
           JOptionPane.showMessageDialog(this,"sericiorese de haber llenado el formulario");
       }
       else
       {
           llenarproveedor();
           pro.insertar(this.c.getConexion(), this.c.getEstado());
           jButton2.setEnabled(true);
           btnagregar.setEnabled(false);
           cancelar.setEnabled(false);
       }
    }//GEN-LAST:event_btnagregarActionPerformed

