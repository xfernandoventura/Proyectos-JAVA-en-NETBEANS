    public void llenartabla()
    {
        //limpiar tabla
        int filas=tabla.getRowCount();
            for (int i = 0;filas>i; i++) {
                tabla.removeRow(0);
            }
            //llenado de tabla
        for(cliente cli:this.clis.getclientes(this.c.getConexion(),this.c.getEstado()))
           {
               tabla.addRow(new Object[]{cli.getnombre(),cli.getapellido(),cli.getdireccion(),cli.gettelefono(),cli.getpais(),cli.getdesc(),cli.getcategoria().getcategoria(),cli.getvisitas()});
           }  
    }

