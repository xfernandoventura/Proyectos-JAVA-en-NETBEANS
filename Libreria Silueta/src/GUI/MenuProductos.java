    public void llenartabla()
    {
        //limpiar tabla
        int filas=tabla.getRowCount();
            for (int i = 0;filas>i; i++) {
                tabla.removeRow(0);
            }
            //llenado de tabla
        for(producto p:this.products)
           {
               tabla.addRow(new Object[]{p.getnombre(),p.getprec_uni()});
           }  
    }
    public void llenarcomboproveedor()
    {
        combopro.removeAllElements();
        for(proveedor p: provs.getproveedores(this.c.getConexion(), this.c.getEstado()))
        {
            combopro.addElement(p.getnombre());
        }
    }
    public void llenarcombocategoria()
    {
        combocat.removeAllElements();
        for(categoria c: cats.getlistacategorias(this.c.getConexion(), this.c.getEstado()))
        {
            combocat.addElement(c.getcategoria());
        }
    }
    
    public void llenarproducto()
    {
        pro.setnombre(txtnombre.getText());
        pro.setdescripcion(txtdescripcion.getText());
        pro.setprec_uni(Double.parseDouble(txtpreciounidad.getText()));
        pro.setunidadmedida(txtunidadmedida.getText());
        pro.setcantidad(Integer.parseInt(txtcantidad.getText()));
        pro.setproveedor(provs.getproveedorbyname(labelproveedor.getText(), this.c.getConexion(), this.c.getEstado()));
        pro.setcategoria(cats.getcategoriabyname(labelcategoria.getText(), this.c.getConexion(), this.c.getEstado()));
    }
    public void ingresardatos()
    {
        txtnombre.setText(pro.getnombre());
        txtdescripcion.setText(pro.getdescripcion());
        txtunidadmedida.setText(pro.getunidadmedida() + "");
        txtpreciounidad.setText(pro.getprec_uni() + "");
        labelproveedor.setText(pro.getproveedor().getnombre());
        labelcategoria.setText(pro.getcategoria().getcategoria());
        txtcantidad.setText(pro.getcantidad() + "");
    }
