    boolean bandera = false;
    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        if(bandera == false)
        {
          combocategoria.setEnabled(true);
          comboproveedor.setEnabled(true);
          btnmodificar.setText("Finalizar Modificado");
          bandera = true;
        }
        else
        {
           if(txtnombre.getText().equals("") || txtdescripcion.getText().equals("") ||
          txtunidadmedida.getText().equals("") || txtpreciounidad.getText().equals("") ||
          labelproveedor.getText().equals("") || labelcategoria.getText().equals("")||
          txtcantidad.getText().equals(""))
          {
           JOptionPane.showMessageDialog(this,"sericiorese de haber llenado el formulario");
          } 
           else
           {
               llenarproducto();
               pro.actualizar(this.c.getConexion(), this.c.getEstado());
               combocategoria.setEnabled(false);
               comboproveedor.setEnabled(false);
               btnmodificar.setText("Modificar");
               llenartabla();
           }
           bandera = false;
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        JOptionPane.showMessageDialog(this,pro.borrar(this.c.getConexion(), this.c.getEstado()));
        txtnombre.setText("");
        txtdescripcion.setText("");
        txtunidadmedida.setText("");
        txtpreciounidad.setText("");
        labelproveedor.setText("");
        labelcategoria.setText("");
        txtcantidad.setText("");
        //llenare de el primer cliente que encuentre para evitar  errores de navegacion cuando el cli este vacio
        for(producto p:this.prods.getlistaproductos(this.c.getConexion(), this.c.getEstado()))
        {
            this.pro = p;
            break;
        }
        ingresardatos();
        this.products = this.prods.getlistaproductos(this.c.getConexion(), this.c.getEstado());
        llenartabla();
    }//GEN-LAST:event_jButton8ActionPerformed

