    private void txtbnombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbnombreKeyReleased
        String letra = "";
        letra = txtbnombre.getText();
        if(letra.equals(""))
        {
           // this.clients.clear();
            this.products = this.prods.getlistaproductos(this.c.getConexion(), this.c.getEstado());
            this.llenartabla();
        }
        else
        {    
           //this.clients.clear();
           this.products = this.prods.getproductosporletra(this.c.getConexion(), this.c.getEstado(),letra);
           this.llenartabla();
           this.pro = this.products.get(0);
           this.ingresardatos();
        } 
    }//GEN-LAST:event_txtbnombreKeyReleased

