    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        this.products = prods.getlistaproductos(this.c.getConexion(), this.c.getEstado());
        if(products.get(this.products.size()-1).getid() == pro.getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al final");
        }
        else
        {
            int index = 0;
            for(producto p:products)
            {
                if(p.getid() == pro.getid())
                {
                    break;
                }
                index += 1;
            }
            pro = products.get(index + 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

