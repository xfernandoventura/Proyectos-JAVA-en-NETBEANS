    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.empleados = emps.getempleados(this.c.getConexion(), this.c.getEstado());
        if(emp.getid() == empleados.get(0).getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al principio");
        }
        else
        {
            int index = 0;
            for(empleado p:empleados)
            {
                if(p.getid() == emp.getid())
                {
                    break;
                }
                index += 1;
            }
            emp = empleados.get(index - 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        this.empleados = emps.getempleados(this.c.getConexion(), this.c.getEstado());
        if(empleados.get(this.empleados.size()-1).getid() == emp.getid())
        {
            JOptionPane.showMessageDialog(this,"ya esta al final");
        }
        else
        {
           int index = 0;
            for(empleado p:empleados)
            {
                if(p.getid() == emp.getid())
                {
                    break;
                }
                index += 1;
            }
            emp = empleados.get(index + 1);
            ingresardatos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

