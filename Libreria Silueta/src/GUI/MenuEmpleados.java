    public void ingresardatos()
    {
        txtnombre.setText(emp.getnombre());
        txtapellido.setText(emp.getapellido());
        txtdireccion.setText(emp.getdireccion());
        txtnacimiento.setText(emp.getfecha_nac());
        txtcontratacion.setText(emp.getfechaa_contratacion());
        txtcontraseña.setText(emp.getclave());
        txtsalario.setText(Double.toString(emp.getSalario()));
        labelcargo.setText(emp.getcargo().getcargo());
        
    }
    
    public void llenarcombo()
    {
        combo.removeAllElements();
        for(cargo car: cargs.getcargos(this.c.getConexion(), this.c.getEstado()))
        {
            combo.addElement(car.getcargo());
        }
    }
    
    public void llenarempleado()
    {
        emp.setnombre(txtnombre.getText());
        emp.setapellido(txtapellido.getText());
        emp.setdireccion(txtdireccion.getText());
        emp.setfecha_nac(txtnacimiento.getText());
        emp.setfecha_contratacion(txtcontratacion.getText());
        emp.setclave(txtcontraseña.getText());
        emp.setSalario(Double.parseDouble(txtsalario.getText()));
        emp.setcargo(cargs.getcargobyname(labelcargo.getText(), this.c.getConexion(), this.c.getEstado()));
        emp.setgerente(gs.getgerentebyid(2, this.c.getConexion(), this.c.getEstado()));
    }
